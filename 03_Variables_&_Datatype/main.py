# declare value

# 01 string write in the text form and use double quotes and single or triple quotes
name = "Ali Hassan"
city = "Karachi"

print("my name is " ,name)
print("I live in ", city)

# 02 integer write in the number form
age= 17
print("my age is ", age)

# 03 float write in the decimal form
percentage = 84.5
print("my percentage is ", percentage)

# 04 boolean write in the true or false form
store=5
num = store > 3
num1 = store < 3
print(num)
print(num1)

# 05 list write in the square brackets and it allow to change the values
fruits0 = ["apple", "banana", "cherry"]
number = [1, 2, 3, 4, 5]
print(fruits0)
print(number)

# 06 tuple write in the round brackets and it does not allow to change the values
fruits = ("apple", "banana", "cherry")
number = (1, 2, 3, 4, 5)
print(fruits)
print(number)

# 07 set write in the curly brackets and it does not allow duplicate values
fruits1 = {"apple", "banana", "cherry"}
number = {1, 2,  3, 4, 5}
print(fruits1)
print(number)

# 08 dictionary write in the curly brackets and it has key and value pair
fruits2 = {"name": "Ali", "age": 17}
print(fruits)


