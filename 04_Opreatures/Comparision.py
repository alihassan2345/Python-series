x=5
y=9

print("x is equal to y: ", x==y) # x is equal to y:  False
print("x is not equal to y: ", x!=y) # x is not equal to y:  True
print("x is greater than y: ", x>y) # x is greater than y:  False
print("x is less than y: ", x<y) # x is less than y:  True
print("x is greater than or equal to y: ", x>=y) # x is greater than or equal to y:  False
print("x is less than or equal to y: ", x<=y) # x is less than or equal to y:  True
